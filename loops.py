# FOR

comidas = ["pizza", "pastel", "kibe"] # Lista de comidas

for comida in comidas: # A cada iteração a variávle fruta assume o valor de um elemento da lista, e após isso
    print(comida)      # o bloco de código dentro do loop é executado

# Loop FOR ele percorre toda a lista da variavél podendo gerar uma iteração, caso seja uma operação matemática ou troca de valores


# WHILE

contador = 0 # contador inicia em 0

while contador < 10: # condição para avaliar se contador é maior ou menor que 10
    print(contador) # Aqui vai imprimir o valor do código
    contador =+ 1 # Caso a condição for menor que 10, ele irá realizar uma iteração adicionando valor 1 a cada volta, até igualar a 10

# Cuidado para ela não se tornar um loop infinito



