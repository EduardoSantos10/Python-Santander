# CRIAR E UTLIZAR MODULOS PERSONALIZADOS

"""
Para criar um módulo personalizado, simplesmente criamos um novo arquivo Python com o nome desejado e definimos as funções, classes e variáveis que queremos incluir no módulo.
"""

def saudar(nome): # ESTOU CRIANDO UM MODULO PARA SAUDAR UMA PESSOA, IMPRIME UMA SAUDAÇÃO
    print(f"Olá, {nome}")


def calcular_somar(a, b): # ESTOU CRIANDO UM MODULO QUE VAI REALIZAR UMA OPERAÇÃO(SOMA)
    return a + b

